			SETB	SEL2
			CLR		SEL1
			JMP		SELJLOOP
SELECT2:
			SETB	SEL3
			CLR		SEL2
			JMP		SELJLOOP
SELECT3:
			SETB	SEL1
			CLR		SEL3
			JMP		SELJLOOP
