			SETB	TR0				;TIMER0 RUN
			SETB	TR1				;TIMER1 RUN
			MOV		R5, #0
			MOV 	R6, #6
			MOV		SPEED, #6
			

LOOP:	
			JNB		P3.0, INVERSOR
			JNB		P3.1, SELECT
			JNB		P3.2, STARTSTOP
			JNB		P3.3, INCREASE
			JNB		P3.4, DECREASE
			JMP		LOOP
INVERSOR:
			JBC		INV, INVERSOR2
			SETB	INV
INVERSOR2:			
			JNB		P3.0, $
			JMP		LOOP

SELECT:
			JB		LOAD1, SELECT1
			JB		LOAD2, SELECT2
			JB		LOAD3, SELECT3
