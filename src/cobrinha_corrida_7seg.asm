BOOT:
		
			SETB	SEL1
			CLR		SEL2
			CLR		SEL3
			SETB	LOAD1
			CLR		LOAD2
			CLR		LOAD3
			CLR		STOP
			CLR		INV
			CLR		SPEEDUP
			CLR		SPEEDOWN
			CLR		SPEEDMAX
			CLR		SPEEDMIN
			CLR		CHANGE
